package OOP3;

public class FinalParentClass {
	
	// this is the class that is for practise of finalkeyword
	// This class has no main method and hence it cannot run

	public void method() {
		System.out.println("Hi this is the method from parent class");
	}
	
	private void method01() {
		System.out.println("This is a private methoc inside the parent class");
	}
	
	protected void method02() {
		System.out.println("This is a protected methoc inside the parent class");
	}
	

}
